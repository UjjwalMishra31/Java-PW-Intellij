package Loops_Practice;

public class ASCII_values_alongwith_characters
{
    public static void main(String[] args)
    {
        for (int i = 65;i<=90;i++)
        {
            System.out.println(i+" "+(char)i);
        }
        for (int i = 97;i<=122;i++)
        {
            System.out.println(i+" "+(char)i);
        }
    }
}
