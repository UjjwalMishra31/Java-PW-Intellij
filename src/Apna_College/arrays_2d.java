package Apna_College;

public class arrays_2d
{
    public static void main(String[] args)
    {
        int[][] studentmarks = {{78,56,97},{77,99,45},{64,78,56}};
        // for decalring 2d arrays use two [] brackets
        System.out.println(studentmarks[0][1]);//student 1 marks 1
        System.out.println(studentmarks[1][1]);//student 2 marks 1
        System.out.println(studentmarks[2][1]);//student 3 marks 1
    }
}
